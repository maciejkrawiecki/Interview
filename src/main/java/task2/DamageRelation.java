package task2;

public class DamageRelation {
    private String name;
    private String url;

    public String getName() {
        return name;
    }

    public DamageRelation setName(String name) {
        this.name = name;
        return this;
    }

    public String getUrl() {
        return url;
    }

    public DamageRelation setUrl(String url) {
        this.url = url;
        return this;
    }
}
